// Declaração de variáveis //
let indice = 13,
  soma = 0,
  K = 0;

// laço while até o tamanho do indice = 13 //
while (K < indice) {
  // incremento de k até o indice //
  K = K + 1;
  // resultado de soma + k armazenado em soma//
  soma = soma + K;
}
// impressão do resultado no terminal da variavel Soma com o valor da soma acumulado//
console.log(`O resultado da soma é: ${soma}.`);
